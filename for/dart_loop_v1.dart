// 코드의 시작점
void main(){
  //반복문
  //반복 적인 패턴이 보인 다면 for, for in ,while,do while 구문을 사용할 수 있다
  //반복문 보통 조건문과  continue, break 와 함께 많이 사용 된다.

  print("홍길동");
  print("홍길동");
  print("홍길동");

  //위 코드 처럼 반복 적인 패턴이 보인 다면 반복문을 사용 하자.
  for(int i =0; i < 10; i++){
    //조건식이 참 일때 수행되는 영역
    print("이순신");
  }
  //무한 반복문을 조심 하자.
  for(int i = 1; i  >=1 ; i++){
    print(i);
  }
}